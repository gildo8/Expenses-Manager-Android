package com.example.gil.expensesmanager.model;

public class SlideMenuItems {

    private int imgId;
    private String title;

    public SlideMenuItems(int imgId, String title) {
        this.imgId = imgId;
        this.title = title;
    }

    public int getImgId() {
        return imgId;
    }

    public void setImgId(int imgId) {
        this.imgId = imgId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
